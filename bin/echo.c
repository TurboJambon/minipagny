#include "libft.h"

int main(int ac, char **av)
{
	int i;

	i = 1;
	while (i < ac)
	{
		ft_putstr(av[i]);
		ft_putchar(' ');
		ft_putchar('u');
		i++;
	}
	ft_putchar('\n');
	return (0);
}