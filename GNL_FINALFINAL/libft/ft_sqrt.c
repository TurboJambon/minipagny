#include "libft.h"

int		ft_sqrt(int nb)
{
	int a;

	if (nb < 0 || nb > 2147395600)
		return (0);
	a = 1;
	while (a * a != nb && a * a <= nb)
		a++;
	if (a * a > nb)
		return (0);
	return (a);
}
